package com.example.kotlinlearning.fragment

import android.os.Bundle
import android.view.LayoutInflater
import android.view.View
import android.view.ViewGroup
import androidx.fragment.app.Fragment
import androidx.recyclerview.widget.LinearLayoutManager
import com.example.kotlinlearning.R
import com.example.kotlinlearning.adapters.AnimalAdapter
import kotlinx.android.synthetic.main.fragment_recycler.*

class RecyclerFragment : Fragment(){

    val animals: ArrayList<String> = ArrayList()
    companion object {
        fun newInstance(): RecyclerFragment {
            return RecyclerFragment()
        }
    }

    override fun onCreateView(inflater: LayoutInflater, container: ViewGroup?, savedInstanceState: Bundle?): View? {
        return inflater.inflate(R.layout.fragment_recycler, container, false)
    }

    override fun onActivityCreated(savedInstanceState: Bundle?) {
        super.onActivityCreated(savedInstanceState)

        addAnimals()
        recyclerList.layoutManager = LinearLayoutManager(context);
        recyclerList.adapter=AnimalAdapter(animals,context);
    }

